package ex;

import java.util.Random;

public class exemplao {

	public static void main(String[] args) {
		
		int[][] m = new int [5][5];
		
		Random r = new Random();
		
		for(int l = 0; l < 5; l++) {
			
			for(int c = 0; c <= 4; c++) {
				m[l][c] = r.nextInt(100);
				System.out.print(m[l][c] + "\t");
			}
			System.out.println();
		}
	}
}