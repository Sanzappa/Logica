public class Processa1{
	public static void main (String[] args){
		//Tipos primitivos
		//ASSCCI
		char c1 = 'x';
		//Inteiros
		byte b1 = 25;
		short s1 = 31548;
		int i1 = 2456877;
		long lo1 = 879879879;
		//Logico
		boolean bo1 = false;
		//Ponto flutuante
		float f1 = 25.45f;
		double d1 = 45687987.456d;
		
		System.out.printf("A variavel c1 tipo char contem %c",c1);
		System.out.printf("\nA variavel b1 tipo byte contem %d",b1);
		System.out.printf("\nA variavel s1 tipo short contem %d",s1);
		System.out.printf("\nA variavel i1 tipo int contem %d",i1);
		System.out.printf("\nA variavel lo1 tipo long contem %d",lo1);
		System.out.printf("\nA variavel bo1 tipo boolean contem %b",bo1);
		System.out.printf("\nA variavel f1 tipo float contem %.2f",f1);
		System.out.printf("\nA variavel d1 tipo double contem %.3f",d1);
	}
}